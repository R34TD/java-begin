public class MathematicsOperation {
    public static void main(String[] args) {
            int a = 3213;
            int b = 324323;

            int A = 230;
            A += 1323;
            System.out.println(A);
            A -= 1323;
            System.out.println(A);
            A *= 1323;
            System.out.println(A);

            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a / b);
            System.out.println(a * b);
            System.out.println(a % b);
    }
}
