public class ComparisonOperation {
    public static void main(String[] args) {
        int Value1 = 2423;
        int Value2 = 2423;

        System.out.println(Value1 > Value2);
        System.out.println(Value1 < Value2);
        System.out.println(Value1 >= Value2);
        System.out.println(Value1 <= Value2);
        System.out.println(Value1 == Value2);
        System.out.println(Value1 != Value2);


    }
}
